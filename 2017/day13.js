const DISPLAY_EXTRA_INFO = true;

function runThroughScannerWithoutGettingCaught (part, inputStr, DEBUG = false) {
  const inputArr = inputStr.split('\r\n');
  
  // DATA STRUCTURES
  const SCANNERS = {};

  // PARSE INPUT DATA
  for (const line of inputArr) {
    const [ depth, range ] = line.split(': ').map(n => +n);
    SCANNERS[depth] = range;
  }

  // HELPER FUNCTION - SIMULATE THE RUN
  function getResultsByStartTime(startTime) {
    let severity = 0;
    let timesCaught = 0;                                                        // needed for part 2, because if you get caught at depth 0,
                                                                                // the severity is 0, but you still got caught
    for (const depth in SCANNERS) {
      const time = +depth + startTime;
      const range = SCANNERS[depth];
      const period = (range - 1) * 2;                                           // if range 1, period 0. every additional range increases by 2
      const pos = time % period;                                                // after every period, pos is back to 0, so take modulo
      if (pos === 0) {
        severity += (+depth * range);
        ++timesCaught;
      }
    }

    return { severity, timesCaught };
  }

  // ANALYZE
  if (part === 1) {                                                             // PART 1: GET SEVERITY AT t = 0

    return getResultsByStartTime(0).severity;

  } else {                                                                      // PART 2: FIND MIN t SUCH THAT timesCaught = 0

    const TIME_AT_START = Date.now();
    console.log('RUNNING PART 2 ANALYSIS (PLEASE WAIT)...');

    // // SLOWER WAY TO DO PART 2: RUN THE SIMULATION AT EVERY t, AND STOP WHEN YOU DON'T GET CAUGHT
    // const LIMIT = Number.MAX_SAFE_INTEGER;
    // for (let t = 0; t <= LIMIT; ++t) {
    //   if (getResultsByStartTime(t).timesCaught === 0) {
    //     console.log(`(RUN TOOK ${(Date.now() - TIME_AT_START)/1000} SECS)`);
    //     return t;
    //   }
    // }

    // FASTER WAY TO DO PART 2: CHECK THAT t IS NOT AT OR EQUAL TO SOME MULTIPLE OF PERIOD GREATER THAN FORBIDDEN TIME
    const FORBIDDEN_TIME_AND_PERIOD = {};
    for (const depth in SCANNERS) {
      const time = +depth;
      const range = SCANNERS[depth];
      const period = (range - 1) * 2;
      const pos = time % period;
      const remainingTime = (period - pos) % period;
      const forbiddenTime = time + remainingTime;
      FORBIDDEN_TIME_AND_PERIOD[depth] = { forbiddenTime, period };
    }

    const LIMIT = Number.MAX_SAFE_INTEGER;
    for (let t = 0; t <= LIMIT; ++t) {
      if (Object.keys(SCANNERS).every(depth => {
        const time = +depth + t;
        const { forbiddenTime, period } = FORBIDDEN_TIME_AND_PERIOD[depth];
        const timeElapsedSinceForbiddenTime = time - forbiddenTime;
        return timeElapsedSinceForbiddenTime < 0 || timeElapsedSinceForbiddenTime % period !== 0;
      })) {
        console.log(`(RUN TOOK ${(Date.now() - TIME_AT_START)/1000} SECS)`);
        return t;
      }
    }

    console.log(`(RUN TOOK ${(Date.now() - TIME_AT_START)/1000} SECS)`);
    throw 'ERROR: NO SOLUTION FOUND';

  }
}

