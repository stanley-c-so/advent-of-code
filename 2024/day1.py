def analyze_two_column_list_of_nums(part, input_str, DEBUG = False):

  # DATA STRUCTURES

  left_nums = []
  right_nums = []

  right_freq = {}


  # PARSE INPUT DATA

  input_arr = input_str.split('\n')
  for line in input_arr:
    [LS, RS] = [ int(n) for n in line.split('   ') ]

    left_nums.append(LS)
    right_nums.append(RS)

    if RS not in right_freq: right_freq[RS] = 0
    right_freq[RS] += 1


  # ANALYZE

  TIME_AT_START = time.time()

  total = 0

  if part == 1:                                     # PART 1: compare lowest left to lowest right, next lowest left to next lowest right, etc.

    left_nums.sort()
    right_nums.sort()

    for i in range(len(left_nums)):
      total += abs(left_nums[i] - right_nums[i])    # add the absolute difference in the comparison to the total

  else:                                             # PART 2: for each left num, we need to know how often it appears in the right side list

    for num in left_nums:
      if num in right_freq:
        total += right_freq[num] * num              # add the product of the num itself with its right side frequency to the total
                                                    # (if num doesn't appear, then its freq is 0, which is a no-op with respect to the total)

  return total


