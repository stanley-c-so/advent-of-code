def complete_the_sequence(part, input_str, DEBUG = False):

  # PARSE INPUT DATA

  input_arr = input_str.split('\n')

  sum = 0

  # ANALYZE

  for line in input_arr:
    nums = [ int(n) for n in line.split(' ') ]
    sequences = [ nums ]
    while not(all([ x == 0 for x in sequences[-1] ])):
      new_row = []
      for i in range(1, len(sequences[-1])):
        new_row.append(sequences[-1][i] - sequences[-1][i - 1])
      sequences.append(new_row)
    sequences[-1].append(0)

    if part == 1:
    
      for i in range(len(sequences) - 2, -1, -1):
        last_num_of_prev_sequence = sequences[i + 1][-1]
        sequences[i].append(sequences[i][-1] + last_num_of_prev_sequence)
      sum += sequences[0][-1]

    else:

      for i in range(len(sequences) - 2, -1, -1):
        first_num_of_prev_sequence = sequences[i + 1][0]
        sequences[i].insert(0, sequences[i][0] - first_num_of_prev_sequence)
      sum += sequences[0][0]

  return sum


